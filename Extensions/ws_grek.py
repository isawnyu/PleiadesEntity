        cval = ord(c)
        if cval in range(880, 1023) or cval in range(7936, 8191):
            b = None
            if 'small' in allow or 'mixed' in allow or 'all' in allow:
                try:
                    b = grek_unaccented_small[c]
                except:
                    pass
